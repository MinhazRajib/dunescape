let glyph_of ch =
  match List.assoc_opt (Char.uppercase_ascii ch) glyphs with
  | Some g -> g
  | None -> List.assoc '?' glyphs

(* Width of one glyph cell (5px glyph + 1px spacing) at [scale]. *)
let advance scale = 6 * scale

let text_width scale s = String.length s * advance scale

(* Draw [s] with its top-left corner at (x, y_top), growing downward, in the
   current Graphics coordinate system (y up). *)
let draw ?(scale = 2) ~x ~y_top color s =
  Graphics.set_color color;
  String.iteri
    (fun i ch ->
      let g = glyph_of ch in
      let gx = x + (i * advance scale) in
      Array.iteri
        (fun row line ->
          String.iteri
            (fun col p ->
              if p = '#' then
                Graphics.fill_rect
                  (gx + (col * scale))
                  (y_top - ((row + 1) * scale))
                  scale scale)
            line)
        g)
    s

let draw_centered ?(scale = 2) ~cx ~y_top color s =
  draw ~scale ~x:(cx - (text_width scale s / 2)) ~y_top color s

(* Shadowed variant for headings. *)
let draw_shadowed ?(scale = 2) ~cx ~y_top ~shadow color s =
  draw_centered ~scale ~cx:(cx + scale) ~y_top:(y_top - scale) shadow s;
  draw_centered ~scale ~cx ~y_top color s
